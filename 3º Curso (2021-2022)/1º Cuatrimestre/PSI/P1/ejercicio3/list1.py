# A. match_ends
# Given a list of strings, return the count of the number of
# strings where the string length is 2 or more and the first
# and last chars of the string are the same.
# Note: python does not have a ++ operator, but += works.
def match_ends(words):
    i = 0
    for palabra in words:  # bucle recorriendo todas las palabras
        longP = len(palabra)
        if longP >= 2:  # si la longitud es mayor o igual que 2
            # si coinciden primer y último caracter
            if palabra[0] == palabra[longP - 1]:
                # sumamos 1 al número de palabras que cumplen las condiciones
                i += 1
    return i


# B. front_x
# Given a list of strings, return a list with the strings
# in sorted order, except group all the strings that begin with 'x' first.
# e.g. ['mix', 'xyz', 'apple', 'xanadu', 'aardvark'] yields
# ['xanadu', 'xyz', 'aardvark', 'apple', 'mix']
# Hint: this can be done by making 2 lists and sorting each of them
# before combining them.
def front_x(words):
    listaX = []  # hacemos una lista con las palabras que empiezan por x
    listaNoX = []  # otra lista para las que no empiezan por x
    # rellenamos las listas iterando por todas las palabras de la lista
    for palabra in words:
        if (palabra[0] == "x"):
            listaX.append(palabra)
        else:
            listaNoX.append(palabra)
    listaX.sort()  # ordenamos alfabeticamente las listas
    listaNoX.sort()
    listaX.extend(listaNoX)  # finalmente concatenamos ambas listas
    return listaX


# C. sort_last
# Given a list of non-empty tuples, return a list sorted in increasing
# order by the last element in each tuple.
# e.g. [(1, 7), (1, 3), (3, 4, 5), (2, 2)] yields
# [(2, 2), (1, 3), (3, 4, 5), (1, 7)]
# Hint: use a custom key= function to extract the last element form each tuple.
def llave(s):  # función que devuelve el último elemento de la tupla
    return s[-1]


def sort_last(tuples):
    # retornamos una lista ordenada de tuplas utilizando
    # el último valor como parámetro para ordenar
    return sorted(tuples, key=llave)


